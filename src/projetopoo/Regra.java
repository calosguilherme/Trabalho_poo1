package projetopoo;

public class Regra {
    private int id;
    private String tipo;
    private int ladoD;
    private int ladoE;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public int getLadoD() {
        return ladoD;
    }

    public void setLadoD(int ladoD) {
        this.ladoD = ladoD;
    }

    public int getLadoE() {
        return ladoE;
    }

    public void setLadoE(int ladoE) {
        this.ladoE = ladoE;
    }
}
